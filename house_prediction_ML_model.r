library(rattle)
library(caret)
library(xlsx)
dat <- read.xlsx("houseprices.xls", sheetIndex = 1)
view(dat)
plot(dat$AREA,dat$PRICE, col = "Red", xlab = "AREA(sq m)", ylab = "PRICE(USD$)")
cor(dat$PRICE,dat$AREA)
model <- lm(PRICE~AREA,data = dat)
abline(model)
summary(model)
a <- data.frame(AREA = c(2000,3300,45000))
result <- predict(model,a)
result
plot(result)